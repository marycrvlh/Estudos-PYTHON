#---- TUPLAS EM PYTHON ----
#as tuplas são semelhantes às listas, mas são imutáveis, ou seja, os dados dentro de uma tupla não podem ser alterados depois de criados. Elas são representadas por parênteses () e os elementos são separados por vírgula. Ela também trás mais segurança para os dados, pois não podem ser modificados acidentalmente.

animais = ('cachorro', 'gato', 'coelho')
print(animais) #mostra a tupla completa
print(animais[0]) #mostra o primeiro elemento da tupla (cachorro), em programação a contagem começa do zero

